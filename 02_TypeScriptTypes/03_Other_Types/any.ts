// 1️⃣ In TypeScript, the any type is a special type that can be used to represent any value, regardless of its type. Variables of type any can be assigned any value without generating type errors, and can be used in any context without type checking.

//↪️ Example: 1


let value: any;

value = 5; // value is now a number
value = 'hello'; // value is now a string
value = true; // value is now a boolean
